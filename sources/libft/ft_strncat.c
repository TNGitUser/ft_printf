#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	cur;

	cur = 0;
	while (s1[cur] != '\0')
		++cur;
	while (n > 0 && *s2 != '\0')
	{
		s1[cur] = *s2;
		++s2;
		--n;
		++cur;
	}
	s1[cur] = '\0';
	return (s1);
}
