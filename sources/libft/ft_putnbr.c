#include <unistd.h>

void	ft_putnbr(int n)
{
	char	c;
	int		sign;

	sign = 1;
	if (n < 0)
	{
		write(1, "-", 1);
		sign = -1;
	}
	c = (n % 10) > 0 ? (n % 10) + '0' : (((n % 10) * -1) + '0');
	n = n / 10 * sign;
	if (n > 0)
		ft_putnbr(n);
	write(1, &c, 1);
}
