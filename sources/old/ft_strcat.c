#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	cur;

	cur = 0;
	while (s1[cur] != '\0')
		++cur;
	while (*s2 != '\0')
	{
		s1[cur] = *s2;
		++cur;
		++s2;
	}
	s1[cur] = '\0';
	return (s1);
}
